-- ========================================
-- Utility Billing System Database Schema  
-- ========================================

CREATE DATABASE IF NOT EXISTS nwc_billing;
USE nwc_billing;

-- Create Users Table
CREATE TABLE users (
  id INT AUTO_INCREMENT PRIMARY KEY,
  username VARCHAR(255) NOT NULL UNIQUE,
  email VARCHAR(255) NOT NULL UNIQUE,
  password VARCHAR(255),
  phone_number VARCHAR(20),
  full_name VARCHAR(255),
  national_id VARCHAR(50),
  avatar_url VARCHAR(500),
  role INT NOT NULL DEFAULT 1,
  is_active BOOLEAN DEFAULT TRUE,
  last_login_at TIMESTAMP NULL,
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  KEY idx_role (role)
);

-- Create Accounts Table
CREATE TABLE accounts (
  id INT AUTO_INCREMENT PRIMARY KEY,
  user_id INT NOT NULL,
  account_number VARCHAR(255) NOT NULL UNIQUE,
  status VARCHAR(50) NOT NULL DEFAULT 'active',
  account_type VARCHAR(50) DEFAULT 'residential',
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  FOREIGN KEY (user_id) REFERENCES users(id) ON DELETE CASCADE
);

-- Create Premises Table
CREATE TABLE premises (
  id INT AUTO_INCREMENT PRIMARY KEY,
  account_id INT NOT NULL,
  address VARCHAR(255) NOT NULL,
  city VARCHAR(100) NOT NULL,
  zone VARCHAR(50),
  postal_code VARCHAR(20),
  connection_number VARCHAR(100),
  building_area DECIMAL(10, 2),
  plot_area DECIMAL(10, 2),
  latitude DECIMAL(10, 8),
  longitude DECIMAL(10, 8),
  premise_type VARCHAR(50),
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  FOREIGN KEY (account_id) REFERENCES accounts(id) ON DELETE CASCADE
);

-- Create Service Agreements Table
CREATE TABLE service_agreements (
  id INT AUTO_INCREMENT PRIMARY KEY,
  premise_id INT NOT NULL,
  agreement_type VARCHAR(100),
  start_date DATE NOT NULL,
  end_date DATE,
  status VARCHAR(50) NOT NULL DEFAULT 'active',
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  FOREIGN KEY (premise_id) REFERENCES premises(id) ON DELETE CASCADE
);

-- Create Meters Table
CREATE TABLE meters (
  id INT AUTO_INCREMENT PRIMARY KEY,
  premise_id INT NOT NULL,
  meter_number VARCHAR(100) NOT NULL UNIQUE,
  meter_type VARCHAR(50),
  diameter_mm INT,
  installation_date DATE NOT NULL,
  status VARCHAR(50) NOT NULL DEFAULT 'active',
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  FOREIGN KEY (premise_id) REFERENCES premises(id) ON DELETE CASCADE
);

-- Create Meter Readings Table
CREATE TABLE meter_readings (
  id INT AUTO_INCREMENT PRIMARY KEY,
  meter_id INT NOT NULL,
  reading_date DATE NOT NULL,
  value DECIMAL(10, 2) NOT NULL,
  uom VARCHAR(20) DEFAULT 'm3',
  status VARCHAR(50) NOT NULL DEFAULT 'active',
  period_start_date DATE,
  period_end_date DATE,
  days_in_period INT,
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  FOREIGN KEY (meter_id) REFERENCES meters(id) ON DELETE CASCADE,
  KEY idx_meter_reading_date (meter_id, reading_date)
);

-- Create Tariff Slabs Table
CREATE TABLE tariff_slabs (
  id INT AUTO_INCREMENT PRIMARY KEY,
  service_type VARCHAR(50) NOT NULL,
  account_type VARCHAR(50),
  consumption_from DECIMAL(10, 2),
  consumption_to DECIMAL(10, 2),
  unit_price DECIMAL(10, 4),
  monthly_service_charge DECIMAL(10, 2),
  sewage_percentage DECIMAL(5, 2),
  vat_percentage DECIMAL(5, 2),
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  KEY idx_service_type (service_type),
  KEY idx_account_type (account_type)
);

-- Create Bills Table
CREATE TABLE bills (
  id INT AUTO_INCREMENT PRIMARY KEY,
  account_id INT NOT NULL,
  bill_date DATE NOT NULL,
  due_date DATE NOT NULL,
  total_amount DECIMAL(10, 2) NOT NULL,
  status VARCHAR(50) NOT NULL DEFAULT 'unpaid',
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  FOREIGN KEY (account_id) REFERENCES accounts(id) ON DELETE CASCADE,
  KEY idx_account_id (account_id),
  KEY idx_status (status),
  KEY idx_bill_date (bill_date)
);

-- Create Bill Segments Table
CREATE TABLE bill_segments (
  id INT AUTO_INCREMENT PRIMARY KEY,
  bill_id INT NOT NULL,
  premise_id INT NOT NULL,
  name VARCHAR(255) NOT NULL,
  consumption DECIMAL(10, 2),
  amount DECIMAL(10, 2) NOT NULL,
  status VARCHAR(50) DEFAULT 'pending',
  remarks VARCHAR(500),
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  FOREIGN KEY (bill_id) REFERENCES bills(id) ON DELETE CASCADE,
  FOREIGN KEY (premise_id) REFERENCES premises(id) ON DELETE CASCADE,
  KEY idx_bill_id (bill_id),
  KEY idx_premise_id (premise_id)
);

-- Create Billable Charges Table
CREATE TABLE billable_charges (
  id INT AUTO_INCREMENT PRIMARY KEY,
  bill_segment_id INT NOT NULL,
  charge_type VARCHAR(100),
  amount DECIMAL(10, 2),
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  FOREIGN KEY (bill_segment_id) REFERENCES bill_segments(id) ON DELETE CASCADE,
  KEY idx_bill_segment_id (bill_segment_id),
  KEY idx_charge_type (charge_type)
);

-- Create Applied Tariff Slabs Table
CREATE TABLE applied_tariff_slabs (
  id INT AUTO_INCREMENT PRIMARY KEY,
  bill_id INT NOT NULL,
  tariff_slab_id INT NOT NULL,
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  FOREIGN KEY (bill_id) REFERENCES bills(id) ON DELETE CASCADE,
  FOREIGN KEY (tariff_slab_id) REFERENCES tariff_slabs(id) ON DELETE CASCADE,
  KEY idx_bill_id (bill_id),
  KEY idx_slab_id (tariff_slab_id)
);

-- Create Payments Table
CREATE TABLE payments (
  id INT AUTO_INCREMENT PRIMARY KEY,
  account_id INT NOT NULL,
  bill_id INT,
  payment_date DATE NOT NULL,
  amount DECIMAL(10, 2) NOT NULL,
  status VARCHAR(50) NOT NULL DEFAULT 'pending',
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  FOREIGN KEY (account_id) REFERENCES accounts(id) ON DELETE CASCADE,
  FOREIGN KEY (bill_id) REFERENCES bills(id) ON DELETE SET NULL,
  KEY idx_account_id (account_id),
  KEY idx_bill_id (bill_id),
  KEY idx_payment_date (payment_date)
);

-- Create Payment Methods Table
CREATE TABLE payment_methods (
  id INT AUTO_INCREMENT PRIMARY KEY,
  account_id INT NOT NULL,
  method_type VARCHAR(50) NOT NULL,
  details TEXT,
  is_primary BOOLEAN DEFAULT FALSE,
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  FOREIGN KEY (account_id) REFERENCES accounts(id) ON DELETE CASCADE,
  KEY idx_account_id (account_id)
);

-- Create User Preferences Table
CREATE TABLE user_preferences (
  id INT AUTO_INCREMENT PRIMARY KEY,
  user_id INT NOT NULL,
  language VARCHAR(5) DEFAULT 'ar',
  theme VARCHAR(50) DEFAULT 'light',
  notifications_enabled BOOLEAN DEFAULT TRUE,
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  UNIQUE KEY unique_user_preference (user_id),
  FOREIGN KEY (user_id) REFERENCES users(id) ON DELETE CASCADE
);

