-- Create Applied Tariff Slabs Table (Ø§Ù„ØªØ¹Ø±ÙŠÙØ§Øª Ø§Ù„Ù…Ø·Ø¨Ù‚Ø© Ø¹Ù„Ù‰ Ø§Ù„ÙØ§ØªÙˆØ±Ø©)
CREATE TABLE applied_tariff_slabs (
  id INT AUTO_INCREMENT PRIMARY KEY,
  bill_id INT NOT NULL,
  tariff_slab_id INT NOT NULL,
  slab_from DECIMAL(10, 2),
  slab_to DECIMAL(10, 2),
  rate_per_unit DECIMAL(10, 4),
  usage_in_this_slab DECIMAL(10, 2),
  service_type VARCHAR(100),
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  FOREIGN KEY (bill_id) REFERENCES bills(id) ON DELETE CASCADE,
  FOREIGN KEY (tariff_slab_id) REFERENCES tariff_slabs(id) ON DELETE RESTRICT
