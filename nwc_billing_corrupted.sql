-- Create Users Table (Ù…Ø¹ Ø¬Ù…ÙŠØ¹ Ø§Ù„ØªØ¹Ø¯ÙŠÙ„Ø§Øª Ø§Ù„Ù…Ù‚ØªØ±Ø­Ø©)
-- Role Constants: 1 = Customer, 2 = Employee, 3 = Admin
CREATE TABLE users (
  id INT AUTO_INCREMENT PRIMARY KEY,
  username VARCHAR(255) NOT NULL UNIQUE,
  email VARCHAR(255) NOT NULL UNIQUE,
  password VARCHAR(255),
  phone_number VARCHAR(20),
  full_name VARCHAR(255),
  national_id VARCHAR(50),
  avatar_url VARCHAR(500),
  role INT NOT NULL DEFAULT 1,
  is_active BOOLEAN DEFAULT TRUE,
  last_login_at TIMESTAMP NULL,
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  KEY idx_role (role)
);

-- Create Accounts Table
CREATE TABLE accounts (
  id INT AUTO_INCREMENT PRIMARY KEY,
  user_id INT NOT NULL,
  account_number VARCHAR(255) NOT NULL UNIQUE,
  status VARCHAR(50) NOT NULL DEFAULT 'active',
  account_type VARCHAR(50) DEFAULT 'residential',
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  FOREIGN KEY (user_id) REFERENCES users(id) ON DELETE CASCADE
);

-- Create Premises Table
CREATE TABLE premises (
  id INT AUTO_INCREMENT PRIMARY KEY,
  account_id INT NOT NULL,
  address VARCHAR(255) NOT NULL,
  city VARCHAR(100) NOT NULL,
  zone VARCHAR(50),
  postal_code VARCHAR(20),
  connection_number VARCHAR(100),
  building_area DECIMAL(10, 2),
  plot_area DECIMAL(10, 2),
  latitude DECIMAL(10, 8),
  longitude DECIMAL(10, 8),
  premise_type VARCHAR(50),
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  FOREIGN KEY (account_id) REFERENCES accounts(id) ON DELETE CASCADE
);

-- Create Service Agreements Table
CREATE TABLE service_agreements (
  id INT AUTO_INCREMENT PRIMARY KEY,
  premise_id INT NOT NULL,
  agreement_type VARCHAR(100),
  start_date DATE NOT NULL,
  end_date DATE,
  status VARCHAR(50) NOT NULL DEFAULT 'active',
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  FOREIGN KEY (premise_id) REFERENCES premises(id) ON DELETE CASCADE
);

-- Create Meters Table (Ù…Ø¹ diameter_mm)
CREATE TABLE meters (
  id INT AUTO_INCREMENT PRIMARY KEY,
  premise_id INT NOT NULL,
  meter_number VARCHAR(100) NOT NULL UNIQUE,
  meter_type VARCHAR(50),
  diameter_mm INT,
  installation_date DATE NOT NULL,
  status VARCHAR(50) NOT NULL DEFAULT 'active',
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  FOREIGN KEY (premise_id) REFERENCES premises(id) ON DELETE CASCADE
);

-- Create Meter Readings Table (Ù…Ø¹ ÙØªØ±Ø© Ø§Ù„Ù‚Ø±Ø§Ø¡Ø©)
CREATE TABLE meter_readings (
  id INT AUTO_INCREMENT PRIMARY KEY,
  meter_id INT NOT NULL,
  reading_date DATE NOT NULL,
  value DECIMAL(10, 2) NOT NULL,
  uom VARCHAR(20) DEFAULT 'm3',
  status VARCHAR(50) NOT NULL DEFAULT 'active',
  period_start_date DATE,
  period_end_date DATE,
  days_in_period INT,
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  FOREIGN KEY (meter_id) REFERENCES meters(id) ON DELETE CASCADE,
  KEY idx_meter_reading_date (meter_id, reading_date)
);

-- Create Tariff Slabs Table (Ù…Ø¹ Ø¬Ù…ÙŠØ¹ Ø§Ù„Ø­Ù‚ÙˆÙ„ Ø§Ù„Ù…Ù‚ØªØ±Ø­Ø©)
CREATE TABLE tariff_slabs (
  id INT AUTO_INCREMENT PRIMARY KEY,
  service_type VARCHAR(100) NOT NULL,
  account_type VARCHAR(50),
  slab_from DECIMAL(10, 2) NOT NULL,
  slab_to DECIMAL(10, 2) NOT NULL,
  rate_per_unit DECIMAL(10, 4) NOT NULL,
  monthly_service_charge DECIMAL(10, 2),
  sewage_percentage DECIMAL(5, 2),
  vat_percentage DECIMAL(5, 2) DEFAULT 15.00,
  effective_date DATE,
  end_date DATE,
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP
);

-- Create Bills Table
CREATE TABLE bills (
  id INT AUTO_INCREMENT PRIMARY KEY,
  account_id INT NOT NULL,
  bill_date DATE NOT NULL,
  due_date DATE NOT NULL,
  total_amount DECIMAL(10, 2) NOT NULL,
  status VARCHAR(50) NOT NULL DEFAULT 'unpaid',
  original_bill_date DATE,
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  FOREIGN KEY (account_id) REFERENCES accounts(id) ON DELETE CASCADE,
  KEY idx_bill_account_date (account_id, bill_date)
);

-- Create Bill Segments Table (ØªÙØ§ØµÙŠÙ„ Ø§Ù„ÙØ§ØªÙˆØ±Ø© Ø­Ø³Ø¨ Ø§Ù„Ù…Ù…ØªÙ„Ùƒ)
CREATE TABLE bill_segments (
  id INT AUTO_INCREMENT PRIMARY KEY,
  bill_id INT NOT NULL,
  premise_id INT NOT NULL,
  segment_type VARCHAR(50),
  current_amount DECIMAL(10, 2),
  status VARCHAR(50) NOT NULL DEFAULT 'active',
  remarks TEXT,
  service_quantity DECIMAL(10, 2),
  uom VARCHAR(20),
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  FOREIGN KEY (bill_id) REFERENCES bills(id) ON DELETE CASCADE,
  FOREIGN KEY (premise_id) REFERENCES premises(id) ON DELETE CASCADE
);

-- Create Billable Charges Table (Ø±Ø³ÙˆÙ… Ø§Ù„ÙØ§ØªÙˆØ±Ø©: Ù…ÙŠØ§Ù‡ØŒ ØµØ±ÙØŒ Ø±Ø³Ù…ØŒ Ø¶Ø±ÙŠØ¨Ø©)
CREATE TABLE billable_charges (
  id INT AUTO_INCREMENT PRIMARY KEY,
  bill_segment_id INT NOT NULL,
  charge_type VARCHAR(50) NOT NULL,
  amount DECIMAL(10, 2) NOT NULL,
  description TEXT,
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  FOREIGN KEY (bill_segment_id) REFERENCES bill_segments(id) ON DELETE CASCADE
